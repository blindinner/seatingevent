export type TranslationKey = keyof typeof translations.en;

export function t(lang: Language, key: TranslationKey): string {
  return translations[lang][key] || translations.en[key] || key;
}

// We need to import these for the hook
import { useCallback, useMemo } from 'react';

// Hook for components - returns stable references that only change when language changes
export function useTranslation(lang: Language) {
  // Memoize the translation function - only recreates when language changes
  const tFn = useCallback(
    (key: TranslationKey) => translations[lang][key] || translations.en[key] || key,
    [lang]
  );

  // Memoize the entire return object - only recreates when language changes
  return useMemo(() => ({
    t: tFn,
    lang,
    isRtl: lang === 'he',
    dir: (lang === 'he' ? 'rtl' : 'ltr') as 'rtl' | 'ltr',
  }), [tFn, lang]);
}
